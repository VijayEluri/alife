function k_phi = sbw4_klookup(q)
p = [3.3000         0         0         0         0         0         0         0         0;
    3.6000    3.4000         0         0         0         0         0         0         0;
    6.0000    4.2000    3.4000         0         0         0         0         0         0;
    12.4000    6.0000    4.8000    3.4000         0         0         0         0         0;
    21.2000   10.8000    7.6000    5.6000         0         0         0         0         0;
    32.4000   17.1000   12.0000    9.2000    6.3000    3.5000         0         0         0;
    46.2000   24.8000   17.7000   14.4000   11.0000    7.1000    3.6000         0         0;
    62.4000   33.9000   24.4000   20.6000   16.9000   12.8000    8.0000    3.7000         0;
    81.1000   44.5000   32.2000   27.8000   23.8000   19.5000   14.6000    9.0000    3.8000];

%i1 = ceil(-q(2)/0.05)+1;
%i2 = floor(q(1)/0.05)+1;
%k_phi = p(i1,i2)+0.1;

n=9;
h = 0.4/(n-1);

q2 = -q(2)/h+1
i1a = max(min(floor(q2)+1,n),1);
i1b = min(i1a+1,n);
w1b = q2-floor(q2)
w1a = 1 - w1b;

q1 = q(1)/h-1
i2a = max(min(floor(q1)+1,n),1);
i2b = min(i2a+1,n);
w2b = q1-floor(q1)
w2a = 1 - w2b;

p_i = [p(i1a,i2a) p(i1a,i2b); p(i1b,i2a) p(i1b,i2b)]
k_phi = [w1a w1b]*p_i*[w2a; w2b]+0.1;

end

